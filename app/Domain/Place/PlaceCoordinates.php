     * 서울 기준 좌표 + 랜덤 오프셋(봇탐지 완화). reverseGeocodingInput 은 임의 지역.
     *
     * @return array{x:string,y:string,bounds:string,reverseGeocodingInput:array{x:string,y:string},ts:string}
     */
    public static function resolve(string $query = ''): array
    {
        $all = self::all();
        $seoul = $all['서울'];
        $off = mt_rand(-10000, 10000) / 10000000;

        $x = number_format((float) $seoul['x'] + $off, 14, '.', '');
        $y = number_format((float) $seoul['y'] + $off, 14, '.', '');

        [$minX, $minY, $maxX, $maxY] = explode(';', $seoul['bounds']);
        $minX = number_format((float) $minX + $off / 2, 14, '.', '');
        $minY = number_format((float) $minY + $off / 2, 14, '.', '');
        $maxX = number_format((float) $maxX + $off / 2, 14, '.', '');
        $maxY = number_format((float) $maxY + $off / 2, 14, '.', '');

        $other = $all[array_rand($all)];

        return [
            'x' => $x,
            'y' => $y,
            'bounds' => "$minX;$minY;$maxX;$maxY",
            'reverseGeocodingInput' => [
                'x' => number_format((float) $other['x'], 6, '.', ''),
                'y' => number_format((float) $other['y'], 6, '.', ''),
            ],
            'ts' => (time() - mt_rand(0, 2592000)) . str_pad((string) mt_rand(100, 999), 3, '0', STR_PAD_LEFT),
        ];
    }

