 *    거리 재정렬로 실제 무위치 모바일 검색 순위와 어긋난다(crm 실측 2026-07-10).
 *    네이버가 쿼리의 지역어로 알아서 정렬하므로 서울 좌표가 실제 순위와 일치한다.
